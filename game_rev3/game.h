void GameStart(void){
  int i, j, k, l;  
  for(i=0; i<ANI_NUM_START; i++){
    int all_block_k[8][8];
    for(j=0; j<8; j++){
      for(k=0; k<8; k++){
        all_block_k[j][k]=str_block[i][j][k];
      }
    }
    for(l=0; l<40; l++){
      light(all_block_k);
      if(game == 0) // ゲームスタート　0：スタート　1：ゲームオーバー
        break;      
    }    
  }
}

void GameOver(void){
  int i,j,k,l;
  for(i=0; i<ANI_NUM_OVER; i++){
    int all_block_k[8][8];
    for(j=0; j<8; j++){
      for(k=0; k<8; k++){
        all_block_k[j][k]=ovr_block[i][j][k];
      }
    }
    for(l=0; l<40; l++){
      light(all_block_k);
    }    
  }
}
